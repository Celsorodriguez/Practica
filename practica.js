let diaDeLaSemana = "Lunes";
switch (diaDeLaSemana) {
    case "Lunes":
        console.log("Hoy se come pizza");
        break;
        case "Martes":
            console.log("Hoy se come arroz de combinación");
            break;
        case "Miércoles":
            console.log("Hoy se come sopa de pata de vaca")
            break;
        case "Jueves":
            console.log("Hoy se come Pure de papa con pollo")
    default:
        console.log("Especifique el día")
}
